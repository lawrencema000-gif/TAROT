/**
 * Convert a card name (e.g., "The Fool", "Three of Cups") to a slug
 * (e.g., "the-fool", "three-of-cups"). Mirrors the cardToSlug() function
 * used in src/pages/TarotCardMeaningPage.tsx.
 */
function nameToSlug(name: string): string {
  return name.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '');
}

/**
 * Map of English card names → Major Arcana id (for lookup by name).
 */
const MAJOR_NAME_TO_ID: Record<string, number> = {
  'the fool': 0,
  'the magician': 1,
  'the high priestess': 2,
  'the empress': 3,
  'the emperor': 4,
  'the hierophant': 5,
  'the lovers': 6,
  'the chariot': 7,
  'strength': 8,
  'the hermit': 9,
  'wheel of fortune': 10,
  'the wheel of fortune': 10,
  'justice': 11,
  'the hanged man': 12,
  'death': 13,
  'temperance': 14,
  'the devil': 15,
  'the tower': 16,
  'the star': 17,
  'the moon': 18,
  'the sun': 19,
  'judgement': 20,
  'judgment': 20,
  'the world': 21,
};

/**
 * Returns the enrichment data for any card by its English name.
 * Tries the Major Arcana lookup first, then falls back to the
 * Minor Arcana slug map. Returns null if the card name is unknown.
 */
export function getEnrichment(cardName: string): TarotEnrichment | null {
  if (!cardName) return null;
  const normalized = cardName.trim().toLowerCase();

  // Major Arcana: try direct name lookup
  const majorId = MAJOR_NAME_TO_ID[normalized];
  if (majorId !== undefined) {
    return majorEnrichment[majorId] ?? null;
  }

  // Minor Arcana: try slug lookup
  const slug = nameToSlug(cardName);
  if (minorEnrichment[slug]) {
    return minorEnrichment[slug];
  }

  // Some Major Arcana may come in as slugs too (e.g., "the-fool")
  const unslugged = slug.replace(/-/g, ' ');
  const majorIdFromSlug = MAJOR_NAME_TO_ID[unslugged];
  if (majorIdFromSlug !== undefined) {
    return majorEnrichment[majorIdFromSlug] ?? null;
  }

  return null;
}
